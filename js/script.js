function openProjectModal(event, category) {
    event.preventDefault();
    const container = document.getElementById('projectModalImages');
    const title = document.getElementById('projectModalTitle');
    container.innerHTML = '';
    title.textContent = category === 'piso' ? 'Visualización Arquitectónica' : 'The Postman';

    projectImages[category].forEach(src => {
        const img = document.createElement('img');
        img.src = src;
        container.appendChild(img);
    });
    document.getElementById('projectModal').style.display = 'flex';
    document.body.classList.add('modal-open');
}

function closeProjectModal() {
    document.getElementById('projectModal').style.display = 'none';
    document.body.classList.remove('modal-open');
}

function openCategoryModal(category) {
    const container = document.getElementById('galleryModalImages');
    const title = document.getElementById('galleryModalTitle');
    container.innerHTML = '';
    title.textContent = categoryTitles[category] || 'Galería';

    const assets = categoryImages[category] || [];
    assets.forEach((src, index) => {
        const isVideo = src.toLowerCase().endsWith('.mp4');
        const item = document.createElement('div');
        item.className = 'gallery-modal-item';
        
        if (isVideo) {
            const videoWrap = document.createElement('div');
            videoWrap.className = 'video-thumbnail-wrap';
            videoWrap.innerHTML = `
                <video src="${src}" muted></video>
                <div class="video-play-overlay">
                    <svg viewBox="0 0 24 24" fill="currentColor"><path d="M8 5v14l11-7z"/></svg>
                </div>
            `;
            videoWrap.onclick = () => openVideoOverlay(src);
            item.appendChild(videoWrap);
        } else {
            const img = document.createElement('img');
            img.src = src;
            img.alt = `Imagen ${index + 1}`;
            img.style.cursor = 'zoom-in';
            img.addEventListener('click', () => openLightbox(category, index));
            item.appendChild(img);
        }
        container.appendChild(item);
    });
    document.getElementById('galleryModal').style.display = 'flex';
    document.body.classList.add('modal-open');
}

function closeGalleryModal() {
    document.getElementById('galleryModal').style.display = 'none';
    document.body.classList.remove('modal-open');
}

function openVideoOverlay(src) {
    const overlay = document.getElementById('videoOverlay');
    const videoSource = document.getElementById('videoSource');
    const video = document.getElementById('demoVideo');

    videoSource.src = src;
    video.load();
    video.play();

    overlay.style.display = 'flex';
    document.body.classList.add('modal-open');
}

function closeVideoOverlay() {
    const overlay = document.getElementById('videoOverlay');
    const video = document.getElementById('demoVideo');

    video.pause();
    video.currentTime = 0;

    overlay.style.display = 'none';
    document.body.classList.remove('modal-open');
}

document.addEventListener('DOMContentLoaded', () => {
    const videoOverlay = document.getElementById('videoOverlay');
    if (videoOverlay) {
        videoOverlay.addEventListener('click', (e) => {
            if (e.target === videoOverlay) closeVideoOverlay();
        });
    }
});

document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') {
        closeProjectModal();
        closeGalleryModal();
        closeVideoOverlay();
        closeLightbox();
    }
    if (e.key === 'ArrowLeft') navigateLightbox(-1);
    if (e.key === 'ArrowRight') navigateLightbox(1);
});

document.querySelectorAll('.modal').forEach(modal => {
    modal.addEventListener('click', (e) => {
        if (e.target === modal) {
            closeProjectModal();
            closeGalleryModal();
        }
    });
});

// Contact form accordion
function toggleContactForm() {
    const wrapper = document.getElementById('contactFormWrapper');
    wrapper.classList.toggle('open');
}

function handleSubmit(event) {
    event.preventDefault();
    const form = event.target;
    const formData = new FormData(form);

    // Get form values
    const nombre = formData.get('nombre');
    const email = formData.get('email');
    const asunto = formData.get('asunto');
    const mensaje = formData.get('mensaje');

    // Create mailto link
    const subject = encodeURIComponent(asunto);
    const body = encodeURIComponent(`Nombre: ${nombre}\nEmail: ${email}\n\n${mensaje}`);
    window.location.href = `mailto:contacto@isidrofernandez.com?subject=${subject}&body=${body}`;

    // Reset form
    form.reset();
}

// Scroll reveal — progressive activation
const revealElements = document.querySelectorAll('.reveal');
const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            revealObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.15, rootMargin: '0px 0px -40px 0px' });

revealElements.forEach(el => revealObserver.observe(el));

// ── LIGHTBOX ──────────────────────────────────────────────────
let _lbImages = [];
let _lbIndex = 0;
let _lbTitle = '';

function openLightbox(category, startIndex = 0) {
    _lbImages = categoryImages[category] || [];
    _lbIndex = startIndex;
    _lbTitle = categoryTitles[category] || '';
    if (!_lbImages.length) return;

    const lb = document.getElementById('lightbox');
    lb.classList.add('active');
    document.body.classList.add('modal-open');
    _lbRender();
}

function closeLightbox() {
    document.getElementById('lightbox').classList.remove('active');
    document.body.classList.remove('modal-open');
}

function navigateLightbox(dir) {
    if (!_lbImages.length) return;
    const img = document.getElementById('lightboxImg');
    img.classList.add('fading');
    setTimeout(() => {
        _lbIndex = (_lbIndex + dir + _lbImages.length) % _lbImages.length;
        _lbRender();
        img.classList.remove('fading');
    }, 180);
}

function _lbRender() {
    document.getElementById('lightboxImg').src = _lbImages[_lbIndex];
    document.getElementById('lightboxImg').alt = `${_lbTitle} — ${_lbIndex + 1}`;
    document.getElementById('lightboxCounter').textContent = `${_lbIndex + 1} / ${_lbImages.length}`;
    document.getElementById('lightboxTitle').textContent = _lbTitle;

    // hide nav arrows when only one image
    const show = _lbImages.length > 1;
    document.getElementById('lightboxPrev').style.visibility = show ? 'visible' : 'hidden';
    document.getElementById('lightboxNext').style.visibility = show ? 'visible' : 'hidden';
}

document.addEventListener('DOMContentLoaded', () => {
    document.getElementById('lightboxClose').addEventListener('click', closeLightbox);
    document.getElementById('lightboxPrev').addEventListener('click', () => navigateLightbox(-1));
    document.getElementById('lightboxNext').addEventListener('click', () => navigateLightbox(1));

    // Close on backdrop click
    document.getElementById('lightbox').addEventListener('click', (e) => {
        if (e.target === document.getElementById('lightbox')) closeLightbox();
    });

    // Mobile swipe
    let _touchX = 0;
    document.getElementById('lightbox').addEventListener('touchstart', (e) => {
        _touchX = e.touches[0].clientX;
    }, { passive: true });
    document.getElementById('lightbox').addEventListener('touchend', (e) => {
        const dx = e.changedTouches[0].clientX - _touchX;
        if (Math.abs(dx) > 50) navigateLightbox(dx < 0 ? 1 : -1);
    }, { passive: true });
});